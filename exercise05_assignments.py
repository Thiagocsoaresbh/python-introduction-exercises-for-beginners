"""
Exercício 5 — Atribuições
Objetivo: Mostrar diferença entre atribuição simples e expressões.
Conceito abordado: atribuição simples e soma com variáveis.
Enunciado:
Crie três variáveis nota1, nota2 e nota3 com valores float.
Depois, calcule a soma e a média das três notas.
Mostre no print cada resultado.
"""

# Atribuições simples
nota1 = 7.5
nota2 = 8.0
nota3 = 6.5

# Expressão: soma = nota1 + nota2 + nota3
soma = nota1 + nota2 + nota3

# Média
media = soma / 3

# Saída
print("Soma das notas:", soma)
print("Média das notas:", media)
