"""
Exercício 2 — Convenções de Nomeação
Objetivo: Entender boas práticas para nomes de variáveis.
Conceito abordado: camelCase e snake_case.
Enunciado:
Crie duas variáveis para armazenar o nome e a idade de uma pessoa.
Use a convenção snake_case para uma variável e camelCase para a outra.
"""

# snake_case (mais comum em Python)
nome_completo = "Maria da Silva"

# camelCase (também aceito, mas menos usado em Python)
idadePessoa = 30

print("Nome:", nome_completo)
print("Idade:", idadePessoa)
